char	*ft_strrchr(const char *s, int c)
{
	int			x;
	const char	*r;

	x = 0;
	r = 0;
	while (s[x])
	{
		if (s[x] == (char)c)
			r = s + x;
		x++;
	}
	if (s[x] == (char)c)
		r = s + x;
	if (r)
		return ((char *)r);
	return (0);
}
